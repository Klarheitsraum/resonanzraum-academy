// Suchfunktion – lokal, kein API-Aufruf
export function sucheImLexikon(query) {
  if (!query || query.trim().length < 2) return [];
  const q = query.toLowerCase().trim();

  // Exakte Treffer zuerst (term stimmt genau überein)
  const exakt = signaturLexikon.filter(e =>
    e.term.toLowerCase() === q
  );

  // Dann: term beginnt mit dem Suchbegriff
  const beginnt = signaturLexikon.filter(e =>
    e.term.toLowerCase().startsWith(q) &&
    e.term.toLowerCase() !== q
  );

  // Wenn exakter Treffer + weitere beginnt-Treffer → alle zusammen
  if (exakt.length > 0 && beginnt.length > 0) return [...exakt, ...beginnt];
  if (exakt.length > 0) return exakt;

  // Dann: term enthält den Suchbegriff
  const enthaelt = signaturLexikon.filter(e =>
    e.term.toLowerCase().includes(q) &&
    !e.term.toLowerCase().startsWith(q)
  );

  // Zusammenführen ohne Duplikate, kein Volltext-Match
  const result = [...beginnt, ...enthaelt];

  // Wenn nichts gefunden, Volltext als Fallback
  if (result.length === 0) {
    return signaturLexikon.filter(e =>
      e.kurz.toLowerCase().includes(q) ||
      e.lang.toLowerCase().includes(q)
    );
  }

  return result;
}
